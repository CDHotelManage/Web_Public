		public List<CdHotelManage.Model.hourse_scheme> DataTableToList(DataTable dt)
		{
			List<CdHotelManage.Model.hourse_scheme> modelList = new List<CdHotelManage.Model.hourse_scheme>();
			int rowsCount = dt.Rows.Count;
			if (rowsCount > 0)
			{
				CdHotelManage.Model.hourse_scheme model;
				for (int n = 0; n < rowsCount; n++)
				{
					model = dal.DataRowToModel(dt.Rows[n]);
					if (model != null)
					{
						modelList.Add(model);
					}
				}
			}
			return modelList;
		}

