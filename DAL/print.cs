        /// <summary>
        /// 得到一个对象实体
        /// </summary>
        public CdHotelManage.Model.print DataRowToModel(DataRow row)
        {
            CdHotelManage.Model.print model = new CdHotelManage.Model.print();
            if (row != null)
            {
                if (row["id"] != null && row["id"].ToString() != "")
                {
                    model.id = int.Parse(row["id"].ToString());
                }
                if (row["pritName"] != null)
                {
                    model.pritName = row["pritName"].ToString();
                }
                if (row["priContent"] != null)
                {
                    model.priContent = row["priContent"].ToString();
                }
            }
            return model;
        }

